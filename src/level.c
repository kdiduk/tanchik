static struct sp1_pss ps;

void level_load(struct sp1_Rect* rect)
{
        const unsigned char* pt = tiles;

        for (uint8_t i = 0; i < TILES_LEN; i++, pt += 8) {
                sp1_TileEntry(TILES_BASE + i, pt);
        }

        ps.bounds = rect;
        ps.flags = SP1_PSSFLAG_INVALIDATE;
        ps.visit = 0;

        sp1_SetPrintPos(&ps, 0, 0);
        sp1_PrintString(&ps, ptiles);
}

